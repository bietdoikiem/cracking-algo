struct ListNode {
  int val;
  ListNode* next;
  ListNode() : val(0), next(nullptr) {}
  ListNode(int x) : val(x), next(nullptr) {}
  ListNode(int x, ListNode* next) : val(x), next(next) {}
};

// Unoptimized way: O(n) time - O (n) space
void reorderList(ListNode* head) {
  ListNode* cur = head;
  stack<ListNode*> s;
  while (cur != nullptr) s.push(cur), cur = cur->next;
  int L = (int)s.size() / 2;
  cur = head;
  while (L > 0) {
    ListNode *last = s.top(), *next = cur->next;
    cur->next = last;
    last->next = next;
    cur = next;
    s.pop(), L--;
  }
  cur->next = nullptr;
}

